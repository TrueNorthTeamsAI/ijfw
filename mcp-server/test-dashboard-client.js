import { test } from 'node:test';
import assert from 'node:assert/strict';
import { readFileSync, statSync } from 'node:fs';
import { join, dirname } from 'node:path';
import { fileURLToPath } from 'node:url';
const __d = dirname(fileURLToPath(import.meta.url));
const HP = join(__d, 'src', 'dashboard-client.html');
const h = readFileSync(HP, 'utf8');
const sz = statSync(HP).size;
test('under 100KB', () => { assert.ok(sz < 102400); });
test('DOCTYPE', () => { assert.ok(h.toLowerCase().trimStart().startsWith('<!doctype html')); });
test('sidebar exists', () => { assert.ok(h.includes('id="sidebar"')); });
test('nav sections exist', () => { assert.ok((h.match(/data-section="/g)||[]).length >= 10); });
test('sidebar 9 items', () => { assert.strictEqual((h.match(/class="sb-item/g)||[]).length, 9); });
test('Lucide ICONS const', () => { assert.ok(h.includes('const ICONS')); });
test('SVGs use currentColor', () => { assert.ok(h.includes('stroke="currentColor"')); });
test('ICON_MAP for routing', () => { assert.ok(h.includes('ICON_MAP')); });
test('EventSource SSE', () => { assert.ok(h.includes('EventSource')); });
test('theme CSS vars', () => { assert.ok(h.includes('--bg-deep')); assert.ok(h.includes('[data-theme="light"]')); });
test('hash routing', () => { assert.ok(h.includes('location.hash')); });
test('no membrowse sidebar item', () => { assert.ok(!h.includes('sb-item" data-section="membrowse"') && !h.includes('sb-item on" data-section="membrowse"')); });
test('memory search', () => { assert.ok(h.includes('data-section="memsearch"')); });
test('projects section', () => { assert.ok(h.includes('data-section="allprojects"')); });
test('settings sections', () => { assert.ok(h.includes('data-section="subs"')); assert.ok(h.includes('data-section="display"')); });
test('sidebar collapsible', () => { assert.ok(h.includes('sbToggle')); });
test('breadcrumb', () => { assert.ok(h.includes('breadcrumb')); });
test('no CDN refs', () => { assert.ok(!h.includes('https://cdn.')); assert.ok(!h.includes('googleapis.com/css')); });
test('system fonts', () => { assert.ok(h.includes('system-ui')); });
test('no Saved framing', () => { assert.ok(!h.includes('Saved $')); });
test('methodology', () => { assert.ok(h.includes('ethodology')); });
test('measured disclaimer', () => { assert.ok(h.includes('easured')); });
